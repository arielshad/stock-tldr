export interface Influencer {
  id: string;
  name: string;
  /** Real person name — only set when different from the brand/channel name. */
  realName?: string;
  handle: string;
  bio: string;
  image: string;
  /** Primary platform — drives the main metric and icon. */
  platform: Platform;
  /** Primary platform URL. */
  url: string;
  /** Follower/subscriber count on primary platform. */
  followers: string;
  /** Raw number for sorting (e.g. 1200000 for "1.2M"). */
  followersRaw: number;
  /** Other platforms this person is active on. */
  links?: { platform: Platform; url: string; label?: string }[];
  /** What kind of content they make. */
  tags: string[];
}

